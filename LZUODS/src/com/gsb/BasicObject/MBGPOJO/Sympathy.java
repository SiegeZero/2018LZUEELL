package com.gsb.BasicObject.MBGPOJO;

import java.util.Date;

public class Sympathy {
    private Integer sysNo;

    private Integer acitivityNo;

    private Date sympathyTime;

    public Integer getSysNo() {
        return sysNo;
    }

    public void setSysNo(Integer sysNo) {
        this.sysNo = sysNo;
    }

    public Integer getAcitivityNo() {
        return acitivityNo;
    }

    public void setAcitivityNo(Integer acitivityNo) {
        this.acitivityNo = acitivityNo;
    }

    public Date getSympathyTime() {
        return sympathyTime;
    }

    public void setSympathyTime(Date sympathyTime) {
        this.sympathyTime = sympathyTime;
    }
}